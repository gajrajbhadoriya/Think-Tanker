<?php

$string = "Hello World"; 

echo "this is strtoupper function: <br>   " . strtoupper($string) . "<br>"; // strtoupper us for all string into upper case 
echo "this is strtolower function:  <br>  " . strtolower($string) . "<br>"; // strlower used for all string into lower case
echo "this is lcfirst function: <br>   " . lcfirst($string) . "<br>"; // lcfirst used for first string into lower case
echo "this is ucfirst function: <br>   " . ucfirst($string) . "<br>"; // ucfirst used for first string into upper case
echo "this is ucwords function: <br>   " . ucwords($string) . "<br>"; // ucwords used for all string first characters into upper case


